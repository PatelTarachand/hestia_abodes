<div class="article-body">
    <p class="lead">Property verification is the most critical step in any real estate transaction. With the increasing complexity of property laws and the rise in fraudulent activities, thorough verification has become essential to protect your investment. This comprehensive guide will walk you through every document and verification you must complete before booking any property.</p>

    <h2>Why Property Verification is Crucial</h2>
    <p>Property verification protects you from:</p>
    <ul>
        <li>Legal disputes and ownership conflicts</li>
        <li>Financial losses due to fraudulent transactions</li>
        <li>Construction delays and project abandonment</li>
        <li>Regulatory non-compliance issues</li>
        <li>Hidden liabilities and encumbrances</li>
        <li>Future legal complications</li>
    </ul>

    <blockquote>
        "Over 60% of property disputes in India arise from inadequate verification during the purchase process. Spending time on verification can save years of legal battles." - Real Estate Legal Expert
    </blockquote>

    <h2>Phase 1: Developer and Project Verification</h2>
    <p>Before evaluating any specific property, verify the developer and project credentials:</p>

    <h3>Developer Background Check</h3>
    <ul>
        <li><strong>Company Registration:</strong> Verify the developer's company registration with the Registrar of Companies (ROC)</li>
        <li><strong>Track Record:</strong> Research previous projects, delivery timelines, and customer feedback</li>
        <li><strong>Financial Stability:</strong> Check the company's financial health through annual reports and credit ratings</li>
        <li><strong>Legal History:</strong> Verify if there are any pending legal cases against the developer</li>
        <li><strong>Awards and Recognition:</strong> Look for industry awards and certifications</li>
    </ul>

    <h3>RERA Registration Verification</h3>
    <p>Under the Real Estate (Regulation and Development) Act, 2016, all projects must be registered with RERA:</p>
    <ul>
        <li>Check RERA registration number on the official Maharashtra RERA website</li>
        <li>Verify project details match the RERA registration</li>
        <li>Confirm the project timeline and delivery dates</li>
        <li>Check for any complaints or violations against the project</li>
        <li>Verify the developer's RERA agent registration</li>
    </ul>

    <h3>Project Approvals Checklist</h3>
    <ul>
        <li><strong>Layout Approval:</strong> From local planning authority (PMRDA/PCMC/PMC)</li>
        <li><strong>Building Plan Approval:</strong> Sanctioned building plans from competent authority</li>
        <li><strong>Environmental Clearance:</strong> If required for the project size and location</li>
        <li><strong>Fire Safety Clearance:</strong> From the Fire Department</li>
        <li><strong>Water and Sewage Approval:</strong> From local water authority</li>
        <li><strong>Electricity Connection:</strong> Load sanction from electricity board</li>
    </ul>

    <h2>Phase 2: Land and Title Verification</h2>
    <p>This is the most critical phase that determines the legal ownership of the property:</p>

    <h3>Title Document Verification</h3>
    <ul>
        <li><strong>Original Title Deed:</strong> Verify the root of title for at least 30 years</li>
        <li><strong>Chain of Title:</strong> Ensure unbroken chain of ownership</li>
        <li><strong>Sale Deed:</strong> Check all previous sale deeds in the chain</li>
        <li><strong>Mutation Records:</strong> Verify property is mutated in favor of current owner</li>
        <li><strong>Khata and Survey Records:</strong> Cross-check with revenue records</li>
    </ul>

    <h3>Encumbrance Certificate</h3>
    <p>Obtain and verify the Encumbrance Certificate for the last 30 years to check:</p>
    <ul>
        <li>All registered transactions on the property</li>
        <li>Any mortgages or loans against the property</li>
        <li>Legal disputes or court cases</li>
        <li>Unpaid dues or liabilities</li>
        <li>Ownership transfers and their legitimacy</li>
    </ul>

    <h3>Survey Settlement Records</h3>
    <ul>
        <li><strong>Survey Number:</strong> Verify the correct survey number</li>
        <li><strong>Sub-divisions:</strong> Check if the land has been properly sub-divided</li>
        <li><strong>Land Classification:</strong> Ensure land is classified for the intended use</li>
        <li><strong>Boundaries:</strong> Verify property boundaries match the documents</li>
        <li><strong>Area Calculation:</strong> Confirm the total area mentioned in documents</li>
    </ul>

    <h2>Phase 3: Legal Compliance Verification</h2>
    <p>Ensure the property complies with all legal requirements:</p>

    <h3>Conversion Certificates</h3>
    <p>If the land was originally agricultural:</p>
    <ul>
        <li>Non-Agricultural (NA) conversion certificate</li>
        <li>Conversion fees payment receipts</li>
        <li>Compliance with conversion conditions</li>
        <li>Permission for change of land use</li>
    </ul>

    <h3>Zoning and Land Use</h3>
    <ul>
        <li><strong>Development Plan:</strong> Check if the land is in a development plan</li>
        <li><strong>Zoning Compliance:</strong> Verify the land use matches the zoning</li>
        <li><strong>FSI Utilization:</strong> Check Floor Space Index compliance</li>
        <li><strong>Setback Requirements:</strong> Verify compliance with setback norms</li>
        <li><strong>Height Restrictions:</strong> Ensure building height compliance</li>
    </ul>

    <h3>Tax and Dues Verification</h3>
    <ul>
        <li><strong>Property Tax:</strong> Verify all property taxes are paid up to date</li>
        <li><strong>Water and Electricity Bills:</strong> Check for any pending dues</li>
        <li><strong>Society Dues:</strong> For apartment purchases, verify society dues</li>
        <li><strong>Development Charges:</strong> Confirm payment of all development charges</li>
        <li><strong>Betterment Charges:</strong> Check if any betterment charges are pending</li>
    </ul>

    <h2>Phase 4: Financial and Banking Verification</h2>
    <p>Verify the financial aspects of the property transaction:</p>

    <h3>Bank Approvals</h3>
    <ul>
        <li>Check if the project is approved by major banks for home loans</li>
        <li>Verify the loan-to-value ratio offered by banks</li>
        <li>Confirm there are no restrictions on bank financing</li>
        <li>Check for any negative listing by banks</li>
    </ul>

    <h3>Payment Structure Verification</h3>
    <ul>
        <li><strong>Payment Schedule:</strong> Verify the payment schedule is linked to construction milestones</li>
        <li><strong>Escrow Account:</strong> Ensure payments are made to RERA-mandated escrow account</li>
        <li><strong>Receipt Documentation:</strong> Maintain proper receipts for all payments</li>
        <li><strong>TDS Compliance:</strong> Verify TDS deduction as per Income Tax rules</li>
    </ul>

    <h2>Phase 5: Construction and Quality Verification</h2>
    <p>For under-construction properties, verify construction quality and progress:</p>

    <h3>Construction Quality Check</h3>
    <ul>
        <li><strong>Structural Engineer Report:</strong> Get an independent structural assessment</li>
        <li><strong>Material Quality:</strong> Verify the quality of construction materials used</li>
        <li><strong>Construction Progress:</strong> Match actual progress with promised timeline</li>
        <li><strong>Safety Measures:</strong> Check if proper safety measures are in place</li>
        <li><strong>Specifications Compliance:</strong> Ensure construction matches agreed specifications</li>
    </ul>

    <h3>Amenities and Infrastructure</h3>
    <ul>
        <li>Verify promised amenities are being constructed</li>
        <li>Check infrastructure development in the area</li>
        <li>Confirm utility connections and capacity</li>
        <li>Verify parking space allocation</li>
        <li>Check common area development</li>
    </ul>

    <h2>Phase 6: Legal Documentation Review</h2>
    <p>Have all legal documents reviewed by a qualified lawyer:</p>

    <h3>Agreement Review</h3>
    <ul>
        <li><strong>Sale Agreement:</strong> Comprehensive review of terms and conditions</li>
        <li><strong>Builder-Buyer Agreement:</strong> Verify RERA-compliant agreement format</li>
        <li><strong>Possession Timeline:</strong> Clear possession and penalty clauses</li>
        <li><strong>Cancellation Terms:</strong> Understand cancellation and refund policies</li>
        <li><strong>Maintenance Clauses:</strong> Review maintenance and society formation terms</li>
    </ul>

    <h3>Insurance and Warranties</h3>
    <ul>
        <li>Construction insurance coverage</li>
        <li>Structural warranty terms</li>
        <li>Defect liability period</li>
        <li>Insurance transfer procedures</li>
    </ul>

    <h2>Red Flags to Watch Out For</h2>
    <p>Be cautious if you encounter any of these warning signs:</p>

    <h3>Documentation Red Flags</h3>
    <ul>
        <li>Reluctance to share original documents</li>
        <li>Photocopies instead of original documents</li>
        <li>Missing or incomplete documentation</li>
        <li>Discrepancies in property details across documents</li>
        <li>Unsigned or improperly executed documents</li>
    </ul>

    <h3>Developer Red Flags</h3>
    <ul>
        <li>No RERA registration or expired registration</li>
        <li>Multiple pending legal cases</li>
        <li>History of project delays or abandonment</li>
        <li>Pressure tactics for immediate booking</li>
        <li>Unrealistic pricing or payment terms</li>
    </ul>

    <h3>Financial Red Flags</h3>
    <ul>
        <li>Demand for cash payments</li>
        <li>No proper receipts or documentation</li>
        <li>Payments to personal accounts instead of company accounts</li>
        <li>Unusual payment structures or demands</li>
        <li>No bank approvals for the project</li>
    </ul>

